<?php
function циклічний_зсув($масив, $k) {
    $довжина = count($масив);

    // Обчислення кількості зсувів
    $k = $k % $довжина; // уникнення зсуву на більше, ніж довжина масиву
    if ($k < 0) {
        $k = $довжина + $k; // перетворення від'ємного зсуву в додатнє
    }

    // Виконання зсуву
    for ($i = 0; $i < $k; $i++) {
        $змінна = $масив[0];
        for ($j = 0; $j < $довжина - 1; $j++) {
            $масив[$j] = $масив[$j + 1];
        }
        $масив[$довжина - 1] = $змінна;
    }

    return $масив;
}

// Початковий масив
$масив = array(1, 2, 3, 4, 5);

// Кількість позицій для зсуву
$k = 2;

// Виклик функції та виведення результату
print_r(циклічний_зсув($масив, $k));
?>
