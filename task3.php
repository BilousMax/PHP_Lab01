<?php
function чи_паліндром($число) {
    $число_рядок = (string)$число; // Перетворення числа у рядок для зручності порівняння
    $довжина = strlen($число_рядок);

    // Порівняння цифр числа, починаючи з країв
    for ($i = 0; $i < $довжина / 2; $i++) {
        if ($число_рядок[$i] !== $число_рядок[$довжина - $i - 1]) {
            return false; // Якщо знайдено нерівні цифри, то число не є паліндромом
        }
    }

    return true; // Усі цифри співпадають, отже число є паліндромом
}

// Задане число
$число = 12321;

// Перевірка, чи є число паліндромом
if (чи_паліндром($число)) {
    echo "Число $число є паліндромом.";
} else {
    echo "Число $число не є паліндромом.";
}
?>
