<?php
function знайти_найбільший($масив) {
    $найбільший = $масив[0]; // Припускаємо, що перший елемент - найбільший

    // Проходимо по всім елементам масиву, починаючи з другого
    for ($i = 1; $i < count($масив); $i++) {
        // Якщо поточний елемент більший за попередній найбільший, замінюємо його
        if ($масив[$i] > $найбільший) {
            $найбільший = $масив[$i];
        }
    }

    return $найбільший;
}

// Заданий масив чисел
$масив = array(5, 3, 9, 2, 8, 7);

// Знаходження найбільшого елемента в масиві
$найбільший = знайти_найбільший($масив);

// Виведення результату
echo "Найбільший елемент в масиві: $найбільший";
?>

