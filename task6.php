<?php
// Заданий масив FR
$FR = array(4, 6);

// Ініціалізуємо мінімальний елемент та його індекси
$мінімальний_елемент = $FR[0];
$індекси_мінімального_елемента = array(0);

// Проходимо по всіх елементах масиву
for ($i = 1; $i < count($FR); $i++) {
    // Порівнюємо поточний елемент з мінімальним
    if ($FR[$i] < $мінімальний_елемент) {
        // Якщо знайдено менший елемент, зберігаємо його значення та індекс
        $мінімальний_елемент = $FR[$i];
        $індекси_мінімального_елемента = array($i);
    } elseif ($FR[$i] == $мінімальний_елемент) {
        // Якщо знайдено елемент з таким самим значенням, додаємо його індекс до списку
        $індекси_мінімального_елемента[] = $i;
    }
}

// Виводимо мінімальний елемент та його індекси
echo "Мінімальний елемент масиву FR: $мінімальний_елемент<br>";
echo "Індекси мінімального елемента: ";
foreach ($індекси_мінімального_елемента as $індекс) {
    echo "$індекс ";
}
?>

